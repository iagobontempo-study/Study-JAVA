package fundamentos;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		//Operadores == != >= > <= <
		int num1 = 97;
		int num2 = 'a';
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
	
		
		int num3 = 55;
		int num4 = 56;
		System.out.println(num3 >= num4);
		System.out.println(num3 <= num4);
		
		System.out.println(3 != 2);
		
		
	}
}
