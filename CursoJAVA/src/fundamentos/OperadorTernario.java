package fundamentos;

public class OperadorTernario {

	public static void main(String[] args) {
		double nota = 7.1;
		String resultado = nota >= 7 ? "Aprovado" : "Recuperacao"; // primeira atribuição(aprovad) verdadeira
		System.out.println(resultado);                             // segunda atribuicao(recuperacao) falsa
		
		
	}
}
